# --- 추천 버튼 ---
if st.button("🎲 메뉴 추천 받기"):
    if not filtered_menus:
        st.warning("추천할 수 있는 메뉴가 없습니다. 제외 메뉴를 줄여보세요.")
    else:
        recommendation = random.choice(filtered_menus)
        st.success(f"오늘의 추천 메뉴는 **{recommendation}** 입니다! 😋")
